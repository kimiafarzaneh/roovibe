-- Setup storage for posts
INSERT INTO storage.buckets (id, name, public) VALUES ('posts', 'posts', true) ON CONFLICT DO NOTHING;

CREATE POLICY "Post images are viewable by everyone."
  ON storage.objects FOR SELECT
  USING ( bucket_id = 'posts' );

CREATE POLICY "Creators can upload post images."
  ON storage.objects FOR INSERT
  WITH CHECK ( bucket_id = 'posts' AND auth.role() = 'authenticated' );

CREATE POLICY "Creators can update their post images."
  ON storage.objects FOR UPDATE
  USING ( bucket_id = 'posts' AND auth.role() = 'authenticated' );

CREATE POLICY "Creators can delete their post images."
  ON storage.objects FOR DELETE
  USING ( bucket_id = 'posts' AND auth.role() = 'authenticated' );
